public class Change {
	 public String dollars;
	 public String halfDollars;
	 public String quarters;
	 public String dimes;
	 public String nickels;
	 public String pennies;
	public String getDollars() {
		return dollars;
	}
	public void setDollars(String dollars) {
		this.dollars = dollars;
	}
	public String getHalfDollars() {
		return halfDollars;
	}
	public void setHalfDollars(String halfDollars) {
		this.halfDollars = halfDollars;
	}
	public String getQuarters() {
		return quarters;
	}
	public void setQuarters(String quarters) {
		this.quarters = quarters;
	}
	public String getDimes() {
		return dimes;
	}
	public void setDimes(String dimes) {
		this.dimes = dimes;
	}
	public String getNickels() {
		return nickels;
	}
	public void setNickels(String nickels) {
		this.nickels = nickels;
	}
	public String getPennies() {
		return pennies;
	}
	public void setPennies(String pennies) {
		this.pennies = pennies;
	}
	
	 
	 
	 
	
	 

}
